  private void init() throws IntrospectionException, UseDBException {
    SwingUtil.centerWithinScreen(this);

    textTemplate.getEditorArea().setComponentPopupMenu(new EditorPopupMenu(textTemplate));
    
    templateLink = new RecordLink();
    templateLink.add("TPL_NAME", textName);
    templateLink.add("TPL_DESCRIPTION", textDescription);
    templateLink.add("TPL_BODY", textTemplate);

    if (tpl_id != null) {
      template = new Template(InternalDatabase.get(), tpl_id);
    }
    else {
      template = new Template(InternalDatabase.get());
      template.setUsrId(Application.get().getUserId());
    }
    templateLink.updateComponents(template);
    checkAllUsers.setSelected(StringUtil.isEmpty(template.getUsrId()));
    checkAllUsers.setEnabled(Application.get().isUserAdmin());
    
    if (!Application.get().isUserAdmin() && StringUtil.isEmpty(template.getUsrId())) {
      textName.setEditable(false);
      textDescription.setEditable(false);
      textTemplate.setEditable(false);
    }

    getRootPane().setDefaultButton(buttonOk);
    getRootPane().getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(cmCancel.getShortCut(), "cmCancel");
    getRootPane().getActionMap().put("cmCancel", cmCancel);
  }
  
  public int getModalResult() {
    return modalResult;
  }
  
