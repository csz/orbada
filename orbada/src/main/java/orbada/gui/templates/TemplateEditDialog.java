private void cmOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmOkActionPerformed
 try {
    templateLink.updateRecord(template);
    if (template.isChanged()) {
      if (tpl_id == null) {
        template.applyInsert();
      }
      else {
        template.applyUpdate();
      }
    }
    modalResult = ModalResult.OK;
    dispose();
  } catch (Exception ex) {
    MessageBox.show(this, stringManager.getString("error"), ex.getMessage(), new int[] {ModalResult.OK});
  }
}//GEN-LAST:event_cmOkActionPerformed

private void checkAllUsersItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_checkAllUsersItemStateChanged
  if (checkAllUsers.isSelected()) {
    template.setUsrId("");
  }
  else {
    template.setUsrId(Application.get().getUserId());
  }
}//GEN-LAST:event_checkAllUsersItemStateChanged
  
