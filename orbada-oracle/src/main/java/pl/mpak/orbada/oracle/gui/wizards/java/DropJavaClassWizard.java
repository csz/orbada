  private final StringManager stringManager = StringManagerFactory.getStringManager(OrbadaOraclePlugin.class);

  private Database database;
  private String schemaName;
  private String javaName;
  
  public DropJavaClassWizard(Database database, String schemaName, String javaName) {
    this.database = database;
    this.schemaName = schemaName;
    this.javaName = javaName;
    initComponents();
    init();
  }
  
  private void init() {
    comboJavas.setModel(new JavaClassComboBoxModel(database));
  }
  
  public void wizardShow() {
    ((JavaClassComboBoxModel)comboJavas.getModel()).change(schemaName);
    ((JavaClassComboBoxModel)comboJavas.getModel()).select(javaName, comboJavas);
  }
  
  public String getDialogTitle() {
    return stringManager.getString("DropJavaClassWizard-dialog-title");
  }
  
  public String getTabTitle() {
    return stringManager.getString("DropJavaClassWizard-tab-title");
  }
  
  public String getSqlCode() {
    return "DROP JAVA CLASS " +SQLUtil.createSqlName(schemaName, comboJavas.getSelectedItem().toString());
  }
  
  public boolean execute() {
    try {
      database.executeCommand(getSqlCode());
      return true;
    } catch (Exception ex) {
      MessageBox.show(this, stringManager.getString("error"), ex.getMessage(), ModalResult.OK, MessageBox.ERROR);
      return false;
    }
  }
  
