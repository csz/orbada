  public FlashbackObjectWizard(Database database, String schemaName, String objectName) {
    this.database = database;
    this.schemaName = schemaName;
    this.objectName = objectName;
    initComponents();
    init();
  }
  
  public FlashbackObjectWizard(Database database, String objectName) {
    this(database, null, objectName);
  }
  
  private void init() {
    schemaName = (schemaName == null ? OracleDbInfoProvider.getCurrentSchema(database) : schemaName);
    comboObjects.setModel(new RecyclebinComboBoxModel(database));
  }
  
  public void wizardShow() {
    ((RecyclebinComboBoxModel)comboObjects.getModel()).change();
    ((RecyclebinComboBoxModel)comboObjects.getModel()).select(objectName, comboObjects);
  }
  
  public String getDialogTitle() {
    return stringManager.getString("FlashbackObjectWizard-dialog-title");
  }
  
  public String getTabTitle() {
    return stringManager.getString("FlashbackObjectWizard-tab-title");
  }
  
  public String getSqlCode() {
    OracleRecyclebinInfo info = (OracleRecyclebinInfo)comboObjects.getSelectedItem();
    if (info != null) {
      return
        "FLASHBACK " +info.getType() +" " +
        SQLUtil.createSqlName(schemaName, comboObjects.getSelectedItem().toString()) +
        " TO BEFORE " +info.getOperation() +
        (!StringUtil.isEmpty(textRename.getText()) ? " RENAME TO " +textRename.getText() : "");
    }
    else {
      return "";
    }
  }
  
  public boolean execute() {
    try {
      database.executeCommand(getSqlCode());
      return true;
    } catch (Exception ex) {
      MessageBox.show(this, stringManager.getString("error"), ex.getMessage(), ModalResult.OK, MessageBox.ERROR);
      return false;
    }
  }
  
