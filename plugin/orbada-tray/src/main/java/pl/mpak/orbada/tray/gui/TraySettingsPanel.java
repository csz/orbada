  public void restoreSettings() {
    checkTrayOn.setSelected(settings.getValue(setTrayOn, false));
  }

  public void applySettings() {
    settings.setValue(setTrayOn, checkTrayOn.isSelected());
    settings.store();
  }

  public void cancelSettings() {
    restoreSettings();
  }
  
