private void cmCompileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmCompileActionPerformed
  if (MessageBox.show((Frame)null, stringManager.getString("ViewSourcePanel-script"), stringManager.getString("ViewSourcePanel-execute-script-q"), ModalResult.YESNO) != ModalResult.YES) {
    return;
  }
  
  try {
    getDatabase().executeScript(syntaxViewSource.getText());
    syntaxViewSource.getStatusBar().getPanel("view-status").setText(stringManager.getString("ViewSourcePanel-exec-properly"));
  } catch (Exception ex) {
    ExceptionUtil.processException(ex);
    syntaxViewSource.getStatusBar().getPanel("view-status").setText(ex.getMessage());
  }
}//GEN-LAST:event_cmCompileActionPerformed

