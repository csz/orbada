  private void init() {
    timer = new Timer(250) {
      {
        setEnabled(false);
      }
      public void run() {
        setEnabled(false);
        requestRefresh = false;
        Query query = getDatabase().createQuery();
        try {
          query.setSqlText(DerbyDbSql.getViewSource());
          query.paramByName("schemaname").setString(currentSchemaName);
          query.paramByName("viewname").setString(currentViewName);
          query.open();
          if (!query.eof()) {
            setEditorText("drop view \"" +currentSchemaName +"\".\"" +currentViewName +"\"\n/\n" +query.fieldByName("viewdefinition").getString() +"\n/");
          } else {
            setEditorText("");
          }
          syntaxViewSource.getStatusBar().getPanel("view-status").setText(" ");
        } catch (Exception ex) {
          ExceptionUtil.processException(ex);
        } finally {
          query.close();
        }
      }
    };
    SwingUtil.addAction(syntaxViewSource.getEditorArea(), cmCompile);
    syntaxViewSource.getStatusBar().addPanel("view-status").setText(" ");
    syntaxViewSource.setDatabase(getDatabase());
    OrbadaDerbyDbPlugin.getRefreshQueue().add(timer);
    new ComponentActionsAction(getDatabase(), syntaxViewSource.getEditorArea(), buttonActions, menuActions, "derbydb-view-source-actions");
  }
  
  public Database getDatabase() {
    return accesibilities.getDatabase();
  }
  
  public String getTitle() {
    return stringManager.getString("ViewSourcePanel-title");
  }
  
  public void refresh() {
    refresh(null, currentSchemaName, currentViewName);
  }
  
  public void refresh(String catalogName, String schemaName, String objectName) {
    if (!currentSchemaName.equals(schemaName) || !currentViewName.equals(objectName) || requestRefresh) {
      currentSchemaName = schemaName;
      currentViewName = objectName;
      if (isVisible()) {
        timer.restart();
      } else {
        requestRefresh = true;
        setEditorText("");
      }
    }
  }
  
  @Override
  public boolean canClose() {
    return true;
  }

  public void close() throws IOException {
    timer.cancel();
    syntaxViewSource.setDatabase(null);
    accesibilities = null;
  }
  
