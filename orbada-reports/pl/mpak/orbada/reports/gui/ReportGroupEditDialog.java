  private void init() throws IntrospectionException, UseDBException {
    dataLink = new RecordLink();
    dataLink.add("OREPG_ID", textId);
    dataLink.add("OREPG_NAME", textName, new FieldRequeiredNotNull(java.util.ResourceBundle.getBundle("pl/mpak/orbada/orbada-reports").getString("group_name")));
    dataLink.add("OREPG_TOOLTIP", textTooltip);
    dataLink.add("OREPG_DESCRIPTION", textDescription);
    
    if (orepg_id != null) {
      data = new ReportsGroupRecord(database, orepg_id);
    } else {
      data = new ReportsGroupRecord(database);
      data.setOrepgId(orepg_orepg_id);
      data.setDtpId(dtp_id);
      data.setSchId(sch_id);
      data.setUsrId(usr_id);
      data.setShared(shared);
    }
    dataLink.updateComponents(data);
    
    getRootPane().setDefaultButton(buttonOk);
    getRootPane().getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(cmCancel.getShortCut(), "cmCancel");
    getRootPane().getActionMap().put("cmCancel", cmCancel);
    
    SwingUtil.centerWithinScreen(this);
  }
  
  private void updateRequest() throws Exception {
    dataLink.updateRecord(data);
    if (data.isChanged()) {
      if (orepg_id == null) {
        data.applyInsert();
        orepg_id = data.getId();
      } else {
        data.applyUpdate();
      }
    }
  }
  
