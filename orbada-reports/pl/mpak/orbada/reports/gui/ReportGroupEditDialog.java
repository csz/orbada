  public static String showDialog(Database database, String orepg_id) throws IntrospectionException, UseDBException {
    ReportGroupEditDialog dialog = new ReportGroupEditDialog(database, orepg_id);
    dialog.setVisible(true);
    return dialog.getModalResult() == ModalResult.OK ? dialog.orepg_id : null;
  }
  
